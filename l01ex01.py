#Desenvolva um algoritmo que receba quatro números inteiros, calcule e mostre a soma desses números.

n1 = int(input("Primeiro numero: "))
n2 = int(input("Segundo numero: "))
n3 = int(input("Terceiro numero: "))
n4 = int(input("Quarto numero: "))

soma = n1 + n2 + n3 + n4
print("A soma desses quatro numeros e igual a ", soma)
