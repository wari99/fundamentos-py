#Faça um algoritmo que receba três notas, calcule e mostre a média aritmética entre elas.

nota1 = float(input("1a Nota: "))
nota2 = float(input("2a Nota: "))
nota3 = float(input("3a Nota: "))

totalnotas = nota1 + nota2 + nota3
#print(totalnotas)

medianotas = totalnotas/3

print("Media Aritmetica: ", medianotas)
